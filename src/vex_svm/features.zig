//! Vexor Feature Set
//!
//! Tracks which Solana protocol features are active at a given slot.
//! Each feature is identified by its on-chain account pubkey (owner =
//! Feature111111111111111111111111111111111111) and carries an activation
//! slot. A query at slot S returns active when activation_slot <= S.
//! The sentinel u64::MAX means the feature is explicitly disabled.
//!
//! Registry is generated from the testnet feature-set floor. @prov:features.registry-source
//! Base58 pubkeys are decoded to 32-byte arrays.
//! Feature-account layout is 9 bytes: 1-byte discriminant (0=pending,
//! 1=activated) followed by an 8-byte LE u64 activation slot.

