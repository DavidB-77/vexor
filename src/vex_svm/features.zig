/// Maps feature pubkeys to the slot at which they were activated.
///
/// A feature is considered active at slot S when its activation_slot <= S.
/// An absent entry (not in the map) means the feature is not activated.
pub const FeatureSet = struct {
    /// pubkey (32 bytes) -> activation slot.
    slots: std.AutoArrayHashMapUnmanaged([32]u8, u64),

    const Self = @This();

    pub fn init() Self {
        return .{ .slots = .{} };
    }

    pub fn deinit(self: *Self, allocator: std.mem.Allocator) void {
        self.slots.deinit(allocator);
    }

    /// Return true if the feature identified by `pubkey` is active at `slot`.
    pub fn isActive(self: *const Self, pubkey: [32]u8, slot: u64) bool {
        const activation = self.slots.get(pubkey) orelse return false;
        if (activation == FEATURE_DISABLED) return false;
        return slot >= activation;
    }

    /// Return the activation slot for `pubkey`, or null if not activated.
    pub fn activationSlot(self: *const Self, pubkey: [32]u8) ?u64 {
        const slot = self.slots.get(pubkey) orelse return null;
        if (slot == FEATURE_DISABLED) return null;
        return slot;
    }

    /// Record that `pubkey` was activated at `slot`. Overwrites any prior value.
    pub fn activate(self: *Self, allocator: std.mem.Allocator, pubkey: [32]u8, slot: u64) !void {
        try self.slots.put(allocator, pubkey, slot);
    }

    /// Mark `pubkey` as permanently disabled (FEATURE_DISABLED sentinel).
    pub fn disable(self: *Self, allocator: std.mem.Allocator, pubkey: [32]u8) !void {
        try self.slots.put(allocator, pubkey, FEATURE_DISABLED);
    }

    /// Return true if a feature is present in the map (regardless of its slot).
    pub fn contains(self: *const Self, pubkey: [32]u8) bool {
        return self.slots.contains(pubkey);
    }

    /// Number of feature entries currently tracked.
    pub fn count(self: *const Self) usize {
        return self.slots.count();
    }

    /// Number of features currently activated (activation_slot != FEATURE_DISABLED).
    pub fn activeCount(self: *const Self) usize {
        var n: usize = 0;
        var it = self.slots.iterator();
        while (it.next()) |entry| {
            if (entry.value_ptr.* != FEATURE_DISABLED) n += 1;
        }
        return n;
    }

    /// Convenience helpers for hot-path feature checks (match vex-023 naming).
    pub fn lthashAccountsAccumulatorActive(self: *const Self, slot: u64) bool {
        return self.isActive(LTHASH_ACCOUNTS_ACCUMULATOR, slot);
    }
    pub fn voteStateLayoutV4Active(self: *const Self, slot: u64) bool {
        return self.isActive(VOTE_STATE_LAYOUT_V4, slot);
    }
    pub fn rejectLegacyVoteInstructionsActive(self: *const Self, slot: u64) bool {
        return self.isActive(REJECT_LEGACY_VOTE_INSTRUCTIONS, slot);
    }
    pub fn dataCompletePlacementActive(self: *const Self, slot: u64) bool {
        return self.isActive(DATA_COMPLETE_SHRED_PLACEMENT, slot);
    }

    /// Load feature activations from a flat slice of (pubkey, slot) pairs.
    /// Each pair is 40 bytes: 32-byte pubkey + 8-byte little-endian slot.
    pub fn loadFromSlice(self: *Self, allocator: std.mem.Allocator, data: []const u8) !void {
        if (data.len % 40 != 0) return error.MalformedFeatureData;
        var i: usize = 0;
        while (i + 40 <= data.len) : (i += 40) {
            const pubkey: [32]u8 = data[i..][0..32].*;
            const slot = std.mem.readInt(u64, data[i + 32 ..][0..8], .little);
            try self.slots.put(allocator, pubkey, slot);
        }
    }

    /// Result of `loadFromAccountsDb`. Convenience for bootstrap logging.
    pub const LoadStats = struct {
        total_known: usize,
        found: usize,
        activated: usize,
        pending: usize,
        skipped_bad_owner: usize,
        skipped_bad_size: usize,
        skipped_parse_error: usize,
    };

    /// Look up every `KNOWN_FEATURES` pubkey in `accounts_db`, parse the
    /// `Feature { activated_at: Option<u64> }` bincode payload, and record
    /// the activation slot (or FEATURE_DISABLED for `None`).
    ///
    /// @prov:features.feature-account-layout
    ///   data.len == 9
    ///   data[0]    : discriminant — 0 = None (pending), 1 = Some(slot)
    ///   data[1..9] : u64 little-endian slot (only meaningful when discriminant == 1)
    ///   owner      : Feature111111111111111111111111111111111111
    /// Pre-seed EVERY known feature into the map as FEATURE_DISABLED (inactive).
    /// This is bank-neutral for reads — `isActive(DISABLED)==false`, identical to
    /// an absent entry — but it guarantees that a feature whose ACCOUNT DOES NOT
    /// EXIST at boot still has a map slot. Without it, such a feature, when its
    /// account is created and activated MID-RUN, would have its account bytes
    /// rewritten on-chain (hash parity holds) but its runtime gate would stay
    /// stale until a restart, because `applyNewFeatureActivations` can only flip
    /// gates already in the map (replay_stage.zig:8031, the else-branch warns
    /// "NOT in boot map — stale until restart"). Pre-seeding makes every flip an
    /// in-place value overwrite of an existing key — no allocation, no rehash, no
    /// pointer invalidation for concurrent wave-pool readers. First real trigger:
    /// SIMD-0449 (account not yet staged on testnet). Idempotent: never clobbers
    /// an entry already present (so it's a no-op when called after population).
    pub fn seedKnownFeaturesDisabled(self: *Self, allocator: std.mem.Allocator) !void {
        for (KNOWN_FEATURES) |kf| {
            if (!self.slots.contains(kf.pubkey))
                try self.slots.put(allocator, kf.pubkey, FEATURE_DISABLED);
        }
    }

    pub fn loadFromAccountsDb(
        self: *Self,
        allocator: std.mem.Allocator,
        accounts_db: *vex_store.accounts.AccountsDb,
    ) !LoadStats {
        var stats: LoadStats = .{
            .total_known = KNOWN_FEATURES.len,
            .found = 0,
            .activated = 0,
            .pending = 0,
            .skipped_bad_owner = 0,
            .skipped_bad_size = 0,
            .skipped_parse_error = 0,
        };

        // Pre-seed all known features as DISABLED so mid-run activation of a
        // feature whose account is created AFTER boot can flip its gate in place
        // (see seedKnownFeaturesDisabled). The loop below overwrites the entries
        // for features whose account exists at boot (activated → slot, pending →
        // DISABLED, i.e. unchanged).
        try self.seedKnownFeaturesDisabled(allocator);

        for (KNOWN_FEATURES) |kf| {
            const key: core.Pubkey = .{ .data = kf.pubkey };
            const view = accounts_db._getRooted(&key) orelse continue;
            stats.found += 1;

            switch (parseFeatureAccount(view.data, view.owner.data)) {
                .activated => |slot| {
                    stats.activated += 1;
                    try self.slots.put(allocator, kf.pubkey, slot);
                },
                .pending => {
                    stats.pending += 1;
                    try self.slots.put(allocator, kf.pubkey, FEATURE_DISABLED);
                },
                .bad_owner => stats.skipped_bad_owner += 1,
                .bad_size => stats.skipped_bad_size += 1,
                .parse_error => stats.skipped_parse_error += 1,
            }
        }

        return stats;
    }
};

