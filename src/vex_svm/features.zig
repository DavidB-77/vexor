    // to_account → bincode Feature{activated_at: Option<u64>} = 1-byte
    // discriminant + 8-byte LE slot; account length preserved. This locks the
    // byte semantics used by replay_stage.applyNewFeatureActivations.
    const activation_slot: u64 = 414_812_256; // first slot of epoch 973
    var data = [_]u8{0} ** FEATURE_ACCOUNT_DATA_LEN; // pending: disc=0
    try std.testing.expectEqual(ParseOutcome.pending, parseFeatureAccount(&data, FEATURE_PROGRAM_ID));

    // the rewrite performed at the boundary:
    data[0] = 1;
    std.mem.writeInt(u64, data[1..9], activation_slot, .little);

    switch (parseFeatureAccount(&data, FEATURE_PROGRAM_ID)) {
        .activated => |s| try std.testing.expectEqual(activation_slot, s),
        else => return error.TestUnexpectedResult,
    }
    // exact bytes: 01 + LE slot
    try std.testing.expectEqual(@as(u8, 1), data[0]);
    try std.testing.expectEqual(activation_slot, std.mem.readInt(u64, data[1..9], .little));

    // live-gate flip path: a pending feature sits in the map as
    // FEATURE_DISABLED; getPtr overwrite must make isActive true at/after
    // the boundary slot and false before it.
    var gpa = std.heap.GeneralPurposeAllocator(.{}){};
    defer _ = gpa.deinit();
    const alloc = gpa.allocator();
    var fs = FeatureSet.init();
    defer fs.deinit(alloc);
    try fs.slots.put(alloc, ENABLE_SHA512_SYSCALL, FEATURE_DISABLED);
    try std.testing.expect(!fs.isActive(ENABLE_SHA512_SYSCALL, activation_slot));
    if (fs.slots.getPtr(ENABLE_SHA512_SYSCALL)) |v| v.* = activation_slot;
    try std.testing.expect(fs.isActive(ENABLE_SHA512_SYSCALL, activation_slot));
    try std.testing.expect(fs.isActive(ENABLE_SHA512_SYSCALL, activation_slot + 1));
    try std.testing.expect(!fs.isActive(ENABLE_SHA512_SYSCALL, activation_slot - 1));
}
