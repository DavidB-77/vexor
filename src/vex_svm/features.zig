/// Result of parsing a single feature account payload.
pub const ParseOutcome = union(enum) {
    activated: u64,
    pending,
    bad_owner,
    bad_size,
    parse_error,
};

/// Parse a single feature account payload. Pure function — testable without
/// standing up an AccountsDb. `data` is the bincode-serialized
/// `Feature { activated_at: Option<u64> }`; `owner` must equal FEATURE_PROGRAM_ID.
pub fn parseFeatureAccount(data: []const u8, owner: [32]u8) ParseOutcome {
    if (!std.mem.eql(u8, &owner, &FEATURE_PROGRAM_ID)) return .bad_owner;
    if (data.len < FEATURE_ACCOUNT_DATA_LEN) return .bad_size;
    return switch (data[0]) {
        0 => .pending,
        1 => .{ .activated = std.mem.readInt(u64, data[1..9], .little) },
        else => .parse_error,
    };
}

