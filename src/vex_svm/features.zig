test "FeatureSet: seedKnownFeaturesDisabled enables mid-run flip for account-absent features (SIMD-0449 class)" {
    var gpa = std.heap.GeneralPurposeAllocator(.{}){};
    defer _ = gpa.deinit();
    const alloc = gpa.allocator();

    var fs = FeatureSet.init();
    defer fs.deinit(alloc);

    // A feature whose account does NOT exist at boot (SIMD-0449 today) is absent
    // before pre-seed → applyNewFeatureActivations' getPtr would miss → the gate
    // could not flip mid-run (the bug this closes).
    try std.testing.expect(!fs.contains(DIRECT_ACCOUNT_POINTERS_IN_PROGRAM_INPUT));
    try std.testing.expect(fs.slots.getPtr(DIRECT_ACCOUNT_POINTERS_IN_PROGRAM_INPUT) == null);

    // Pre-seed: every known feature now has a DISABLED entry. Bank-neutral —
    // isActive stays false (== absent) — but the in-place flip path is reachable.
    try fs.seedKnownFeaturesDisabled(alloc);
    try std.testing.expectEqual(@as(usize, KNOWN_FEATURES.len), fs.count());
    try std.testing.expectEqual(@as(usize, 0), fs.activeCount()); // all DISABLED
    try std.testing.expect(fs.contains(DIRECT_ACCOUNT_POINTERS_IN_PROGRAM_INPUT));
    try std.testing.expect(!fs.isActive(DIRECT_ACCOUNT_POINTERS_IN_PROGRAM_INPUT, 999_999_999));

    // Simulate applyNewFeatureActivations' in-place flip at the activation slot.
    const boundary: u64 = 418_000_000;
    const v = fs.slots.getPtr(DIRECT_ACCOUNT_POINTERS_IN_PROGRAM_INPUT) orelse return error.NotSeeded;
    v.* = boundary; // no allocation, no rehash → safe vs concurrent readers
    try std.testing.expect(!fs.isActive(DIRECT_ACCOUNT_POINTERS_IN_PROGRAM_INPUT, boundary - 1));
    try std.testing.expect(fs.isActive(DIRECT_ACCOUNT_POINTERS_IN_PROGRAM_INPUT, boundary));

    // Idempotent + non-clobbering: a second seed must NOT reset the active gate.
    try fs.seedKnownFeaturesDisabled(alloc);
    try std.testing.expect(fs.isActive(DIRECT_ACCOUNT_POINTERS_IN_PROGRAM_INPUT, boundary));
    try std.testing.expectEqual(@as(usize, 1), fs.activeCount());
}

